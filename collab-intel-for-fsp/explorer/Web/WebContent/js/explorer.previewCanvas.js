define( ['jquery', 'explorerBase'], ( function ( $, base )
{
	'use strict';

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	// public properties

	var my = {};

	my.element = null;
	my.canvasContext = null;

	my.canvasWidth = 0;
	my.canvasHeight = 0;

	my.backgroundColor = 'rgb(20,20,20)';

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	// private properties

	var m_base = base;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	my.initialize = function ( elementId )
	{
		my.element = $( '#' + elementId )
			.css( 'position', 'fixed' )
			.css( 'bottom', '0' )
			.css( 'left', '0' )
			.show();

		my.canvasContext = my.element[0].getContext( '2d' );

		return;
	};

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	my.redraw = function ()
	{
		my.canvasContext.save();

		// wipe the background
		my.canvasContext.fillStyle = my.canvasBackgroundColor;
		my.canvasContext.fillRect( 0, 0, my.canvasWidth, my.canvasHeight );

		// finish up
		my.canvasContext.restore();

		return;
	};

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	my.resetView = function ()
	{
		my.redraw();

		return;
	};

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	return my;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

} ) );

